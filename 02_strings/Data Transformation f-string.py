#f-string
name = "Alice"
age = 30
is_student = False
print("My name is " + name +", I am " + str(age) + " years old, and I am a student: " + str(is_student) + ".")
print(f"My name is {name}, I am {age} years old, and I am a student: {is_student}.")


print(f"2 + 3 = {2 + 3}")
print(f"{{This is me}}")#To include literal curly braces in an f-string, you can double them. In this case, `{{` and `}}` will be rendered as `{` and `}` in the output.






